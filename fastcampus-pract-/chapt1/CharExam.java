public class CharExam {
    public static void main(String[] args) {
        int a,b,c,d,e;
        a = '1';
        b = '2';
        c = '3';
        d = '4';
        e = '5';
        int sum = a+b+c+d+e;    // 0 = 48 /48 * 5
        System.out.println("'1'+'2'+'3'+'4'+'5' = " + (sum-48 * 5));
    }
}
